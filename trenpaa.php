          <p class="isicontent style1">
            Hidayatullah.com – Pakar manajemen pendidikan anak Sofia Hartati mengaku   resah dengan tingginya tren aksi pornografi dan pornoaksi remaja dan   anak di bawah umur di Jakarta.<br />
            <br />
            Komisi Perlindungan Anak Indonesia menyebutkan setidaknya ada<br />
            84 laporan pornografi dan pornoaksi yang masuk ke KPAI selama Oktober   2013. Seluruhnya dilakukan oleh anak-anak dari kalangan pelajar di bawah   umur, khususnya di Jakarta.<br />
            <br />
            “Sungguh ini sangat memprihatinkan,” kata Sofia Hartati dalam perbincangan dengan <a href="http://hidayatullah.com" target="_blank">hidayatullah.com</a>, Sabtu (16/11/2013).<br />
            <br />
            Hartati menjelaskan, kondisi saat ini anak anak dan remaja sedang   dihadapkan pada tantangan moral yang sanga berat akibat stimulus yang   datang terlalu banyak yang dominan melalui media.<br />
            <br />
            “Mereka berbuat seperti karena pengaruh media juga. Media telah banyak   rupa, beragam gadget, tayangan TV lokal dan internasional yang sangat   massif tanpa ada sensor yang serius,” terang Hartati.<br />
            <br />
            Dekan Fakultas Ilmu Pendidikan Universitas Negeri Jakarta (UNJ) ini   menilai maraknya gadget teknologi informasi bukan berarti menjadi   sesuatu yang harus dilarang karena menurutnya kondisi itu memang tidak   bisa dielakkan. Ia memandang membendung hal itu menjadi pihak otoritas   baik orangtua, pemerintah, dan masyarakat.<br />
            <br />
            “Semua harus betul betul sama sama punya portal kesadaran bagaimana melindungi anak anak dari pengaruh tersebut,” ungkapnya.<br />
            <br />
            Generasi muda saat ini, lanjut Hartati, tidak bisa dilarang mengakses   teknologi. Tetapi orangtua harus menyadari bahwa hal itu perlu   pendampingan.<br />
            <br />
            Ia pun mendesak peran pemerintah melalui komisi penyiaran untuk selektif   dalam memberikan izin tayangan yang sifatnya tidak positif untuk anak   bangsa dan Pastinya utk Keluarga Muslim.*<br />

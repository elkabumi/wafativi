		  <span class="style3"><strong>Orang yang mengajak kepada kebaikan, akan mendapat pahala sebanyak pahala yang diperoleh orang-orang yang mengikutinya</strong>           </span></p>
        <p align="justify" class="style3" id="yui_3_16_0_1_1407306061906_1953" dir="rtl"><br />
          <span class="style2">Telah menceritakan kepadaku Zuhair bin Harb telah menceritakan kepada kami Jarir bin ‘Abdul Hamid dari Al A’masy dari Musa bin ‘Abdullah bin Yazid dan Abu AdhDhuhadari ‘Abdurrahman bin Hilal Al ‘Absi dari Jarir bin ‘Abdullah dia berkata:</span></p>
        <p align="justify" class="style2" id="yui_3_16_0_1_1407306061906_2006"><span id="yui_3_16_0_1_1407306061906_2005">Pada suatu ketika, beberapa orang Arab badui datang menemui Rasulullah shallallahu ‘alaihi wasallam dengan mengenakan pakaian dari bulu domba (wol). Lalu Rasulullah memperhatikan kondisi mereka yang menyedihkan. Selain itu, mereka pun sangat membutuhkan pertolongan. Akhirnya, Rasulullah shallallahu ‘alaihi wasallam menganjurkan para sahabat untuk memberikan sedekahnya kepada mereka. Tetapi sayangnya, para sahabat sangat lamban untuk melaksanakan anjuran Rasulullah itu, hingga kekecewaan terlihat pada wajah beliau. Jarir berkata: Tak lama kemudian seorang sahabat dari kaum Anshar datang memberikan bantuan sesuatu yang dibungkus dengan daun dan kemudian diikuti oleh beberapa orang sahabat lainnya. Setelah itu, datanglah beberapa orang sahabat yang turut serta menyumbangkan sedekahnya (untuk diserahkan kepada orang-orang Arab badui tersebut) hingga tampaklah keceriaan pada wajah Rasulullah shallallahu ‘alaihi wasallam. Kemudian Rasulullah shallallahu ‘alaihi wasallam bersabda: Barang siapa dapat memberikan suri tauladan yang baik dalam Islam, lalu suri tauladan tersebut dapat diikuti oleh orang-orang sesudahnya, maka akan dicatat untuknya pahala sebanyak yang diperoleh orang-orang yang mengikutinya tanpa mengurangi sedikitpun pahala yang mereka peroleh. Sebaliknya, barang siapa memberikan suri tauladan yang buruk dalam Islam, lalu suri tauladan tersebut diikuti oleh orang-orang sesudahnya, maka akan dicatat baginya dosa sebanyak yang diperoleh orang-orang yang mengikutinya tanpa mengurangi dosa yang mereka peroleh sedikitpun</span></p>
        <p align="justify" class="style3" id="yui_3_16_0_1_1407306061906_3137">(Shahih Muslim 1017-15)</p>

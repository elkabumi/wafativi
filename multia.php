        <p align="center" class="style2"><img src="http://www.wafativi.com/images/multimedia%20besar.jpg" /></p>
        <p align="justify" class="style2"><br />
          <br />
           
          <span class="style1">Bahasa pikiran atau lebih lengkapnya sering disebut sebagai Bahasa Pemrograman Pikiran adalah hasil encoding dari informasi yang diterima panca indera kita yaitu : Penglihatan (visual), Pendengaran (Auditory), Sentuhan (kinesthetic), Penciuman (olfatory), Pencecapan (Gusthatory)<br />
          <br />
          Dengan kata lain otak kita bekerja dengan 5 bahasa indra tersebut, karena setiap informasi yang masuk dalam otak manusia (secara normal) sejatinya hanya berasal dari 5 indra tersebut. Baik sengaja maupun tidak sengaja semua yang ada dalam pikiran kita pasti dalam bentuk kode yang disesuaikan dengan panca indra kita. Ketika kita berfikir tentang lagu, kita tidak memproses tulisan L-A-G-U, tapi biasanya yang muncul dalam pikiran kita saat itu adalah “SUARA” lagu yang paling berkesan buat kita. Ketika kita pikirkan Ayam Goreng, kita tidak sedang memproses tulisan A-Y-A-M-G-O-R-E-N-G, dalam otak kita, tapi yang muncul justru “GAMBAR” ayam goring atau “SUARA” ayam ketika digoreng, suara adalah hasil encoding telinga, gambar adalah hasil encoding mata. Sebuah sumber informasi yang bisa memenuhi unsur-unsur kebutuhan Bahasa program otak kita akan diserap dengan sempurna oleh otak kita, dan akhirnya mempengaruhi perilaku kita. Masih ingat pada pembahasan sebelumnya saya sampaikan cara mempengaruhi manusia ada :
           
          <br />
          <br />
          1.      Cara Repetisi / informasi yang diulang-ulang<br />
          2.      Kesamaan identitas à kurang praktis<br />
          3.      Intensitas emosi yang tinggi à cepat dan mudah<br />
          4.      Figur Otoritas à cepat dan mudah<br />
          5.      Bypass filter logika à Paling efektif dan cepat
           
          <br />
          <br />
          Hari ini kita dihadapkan pada pilihan media informasi yang semakin variatif, sehingga muncullah istilah multi media : adalah media /sumber informasi yang mampu memenuhi lebih dari 1 kebutuhan indra manusia (multi). Televisi adalah salah satu contoh Media yang bersifat multi / multi media yang paling banyak dijangkau, efektif dan sangat mempengaruhi kita. Kenapa sangat efektif ? karena TV paling mampu memenuhi kebutuhan encoding informasi dalam otak kita secara lengkap (ada unsur suara, gambar, gerak, emosi) mungkin hanya bau-bauan yang belum bisa dihadirkan TV, tapi itu sudah lebih dari cukup untuk memperbudak penontonnya. Para ahli menemukan bahwa hanya dengan menonton TV selama 30 detik, TV mampu menurunkan gelombang otak manusia ke level yang lebih rendah, dan akhirnya kehilangan daya kritisnya.
           
          <br />
          <br />
          Ada sebuah joke yang meski terkesan agak mengada-ada, tapi saya rasa joke ini sangat serius dan tepat “Kualitas masyarakat sebuah Negara, bisa dilihat dari kualitas tayangan televisinya”
           
          <br />
          <br />
          Tidak heran kenapa Negara-negara besar seperti Amerika memberi perhatian yang serius pada kualitas tayangan televise dinegaranya. Pun mereka juga membuat dengan serius tontonan/film2 yang didesain menarik, box office dan ditujukan agar ditonton orang2 sedunia. Karena dengan film2 yang dibuat mereka mampu membentuk mindset masyarakat dunia. Sudah dapat duit, sekaligus mengendalikan pikiran dunia. Sebut saja  : Independence Day (dibuat 4 juli, dengan pesan Kemerdekaan Amerika identic dg Kemerdekaan Dunia, dan Amerika lah yang membebaskan dunia dari penjajahan makhluk asing), Armageddon, Rambo, Transformer. Dll
           
          <br />
          <br />
          Untuk itu kita harus mengimbangin derasnya informasi dari multimedia yang ada disekitar kita, bukan dengan menghentikan multimedia itu sendiri, tapi dengan menyediakan multimedia yang mampu mengimbangi pengaruh multimedia pada umumnya. <br />

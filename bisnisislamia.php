    <div class="style2"> <strong>a. UNSUR DAYA PIKIR</strong></div>
    <div class="style2"> Daya pikir merupakan sumber dari  awal kreasi  dan temuan serta yang  terpenting kemajuan ujung tombak suatu umat.<br />
      <br />
      <div style="text-align:center"> <a href="http://3.bp.blogspot.com/-gYdwCtdI0HQ/UBOCgl3agJI/AAAAAAAAA0k/U3ErV780fxg/s1600/zzz.jpg" imageanchor="1"><img src="http://3.bp.blogspot.com/-gYdwCtdI0HQ/UBOCgl3agJI/AAAAAAAAA0k/U3ErV780fxg/s1600/zzz.jpg" alt="" border="0" /></a></div>
      <strong>Firman Allah SWT: </strong></div>
    <div class="style2"> <strong><em>“…Sesungguhnya,  Allah tidak akan mengubah keadaan suatu kaum sehingga mereka mengubah keadaan  yang ada pada diri mereka sendiri…”</em></strong>  <strong>(Ar-Rad: 11)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Seharusnya seorang muslim dapat  berpikir bersumber pada wahyu disertai kecakapan dalam mengamati keadaaan  sekitarnya, berkaitan dengan bisnis, Al-quran sebagai wahyu Allah menunjukkan  sejumlah hal yang penting diantaranya sebagai berikut:</div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Seruan Pengadaan Pangan Berkualitas:</strong></div>
    <div class="style2"> “ <strong><em>Hai sekalian manusia, makanlah yang halal lagi baik dari  apa yang terdapat di bumi dan janganlah kamu mengikuti langkah-langkah setan…”</em></strong> <strong>(Al-Baqarah:  168)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Seruan Pengadaan Pakaian Berkualitas:</strong></div>
    <div class="style2"> <strong><em>“ Hai  anak Adam, sesungguhnya Kami telah menurunkan kepadamu pakaian untuk menutupi  auratmu dan pakaian indah untuk perhiasan…”</em></strong> <strong>(Al-A’raaf: 26)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Anjuran Pengadaan Jasa Transportasi</strong></div>
    <div class="style2"> <strong><em>“ Dan  ia (binatang itu) mengankut beban-bebanmu ke suatu negeri yang kamu tidak  sanggup sambil kepadanya melainkan dengan kesukaran-kesukaran (yang memayahkan)  diri. Sesungguhnya, Allah benar-benar Maha Pengasih lagi Maha Penyayang dan  (Dia telah menciptakan) kuda, bighal, dan khimar agar kamu menungganginya…”</em></strong> <strong>(An - Nahl:  7-8)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Anjuran Pengadaan Jasa Perdagangan</strong></div>
    <div class="style2"> <strong><em>“…  Allah telah menghalalkan jual beli dan mengharamkan riba…”</em> (Al-Baqarah: 275)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Anjuran Pengadaan Industri Peternakan dan  Perikanan</strong></div>
    <div class="style2"> <strong><em>“  Makanlah dan gembalakanlah binatang-binatang ternakmu. Sesungguhnya, pada yang  demikian itu terdapat tanda-tanda kekuasaan Allah bagi orang-orang yang  berakal.”</em> (Thaahaa:  54)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Dorongan Aktivitas Pencerdasan Umat</strong></div>
    <div class="style2"> <strong><em>“  Tidak sepatutnya bagi orang-orang mukmin itu pergi semuanya (ke medan perang).  Mangapa tidak pergi dari tiap-tiap golongan di antara mereka beberapa orang  untuk memperdalam pengetahuan mereka tentang agama dan untuk memberi peringatan  kepada kaumnya apabila mereka telah kembali kepadanya, supaya mereka itu dapat  menjaga dirinya.”</em> (At-Taubah: 122)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong>Dorongan Pengadaan Keodokteran dan  Pengobatan</strong></div>
    <div class="style2"> <strong><em>“  …Dari perut lebah itu keluarlah minuman (madu) yang bermacam-macam warnanya,  didalamnya terdapat obat yang menyembuhkan bagi manusia. Sesungguhnya pada yang  denikian itu benar-benar terdapat tanda (kebesaran Tuhan) bagi orang yang  memikirkan.”</em>  (An-Nahl:  69)</strong></div>

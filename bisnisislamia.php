      <div class="style2"> <strong>1. MENGETAHUI Bisnis Islami = Wirausaha = Wiraswasta =  Entrepreneurship = Kemandirian </strong></div>
      <div>
        <div style="text-align:center"> <a href="http://3.bp.blogspot.com/-hKfMalc4kdk/UBOA4fpuBPI/AAAAAAAAA0U/9RT--Xcr1tc/s1600/rambu-rambu-islami.jpg" imageanchor="1"><img src="http://3.bp.blogspot.com/-hKfMalc4kdk/UBOA4fpuBPI/AAAAAAAAA0U/9RT--Xcr1tc/s320/rambu-rambu-islami.jpg" alt="" width="320" height="226" border="0" /></a></div>
        <br />

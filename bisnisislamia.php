    <div class="style2"> <strong>d. UNSUR INTUISI </strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Jika ditelusuri lebih jauh lagi  sebenarnya ada faktor lain disamping pemikiran, keterampilan dan sikap mental  yang menentukan keberhasilan seseorang. Faktor lain itu tidak lain adalah <strong>intuisi atau</strong> <strong>kewaspadaan</strong> (<strong>Soesarsono,  1996).</strong><br />
      <div style="text-align:center"> <a href="http://4.bp.blogspot.com/-Ku9ad7KcyFQ/UBOHFHvUSEI/AAAAAAAAA1U/xwV_oolT7QE/s1600/cara-menghindari-guru-ngasih-pr1.jpg" imageanchor="1"><img src="http://4.bp.blogspot.com/-Ku9ad7KcyFQ/UBOHFHvUSEI/AAAAAAAAA1U/xwV_oolT7QE/s320/cara-menghindari-guru-ngasih-pr1.jpg" alt="" width="320" height="186" border="0" /></a></div>
    </div>
    <div class="style2"> Intuisi atau lebih dikenal dengan <strong><em>feeling</em></strong> adalah sesuatu yang <strong>abstrak, sulit  digambarkan</strong>, namun acapkali menjadi <strong>kenyataan jika dirasakan serta diyakini</strong> <strong>benar lalu  diusahakan</strong>.</div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Dalam perspektif Islam, intuisi  dapat dinilai <strong>sebagai  bagian lanjut dari pemikiran dan sikap mental maju yang telah dimiliki oleh  seorang muslim.</strong> Seorang muslim memang dituntut untuk mengaplikasikan  pemahaman Islam dalam menjalankan kegiatan hidupnya. Proses aplikasi ini dapat  dilakukan diantaranya dengan cara menumbuhkan kesadaran dan melatih kepekaan  perasaan.</div>

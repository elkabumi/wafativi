      <div class="style2"> ·<strong>Bisnis  Islami</strong> yaitu serangkaian aktivitas bisnis dalam berbagai bentuknya  yang tidak dibatasi jumlah (kuantitas), kepemilikan hartanya (barang/jasa)  termasuk profitnya, namun <strong>dibatasi dalam  cara perolehan dan pendayagunaan hartanya (ada aturan halal dan haram)</strong></div>
      <div class="style2"> <br />
        <a name="more" id="more"></a></div>
      <div class="style2"> ·<strong>Wiraswasta </strong>yaitu bisnis yang lebih menekankan  kepada <strong>sifat-sifat</strong> keberanian,  keutamaan  dan keteladanan dalam  mengambil risiko yang <strong>bersumber pada  kemampuan sendiri </strong>mencakup semua orang dan dalam berbagai bidang  perkerjaan</div>
      <div class="style2"> <br />
      </div>
      <div class="style2"> ·<strong>Wirausaha</strong> = wiraswasta tapi lebih menekankan  pada <strong>bisnis yang  dijalankan </strong>oleh swasta,koperasi ataupun BUMN</div>

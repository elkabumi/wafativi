    </div>
    <div class="style2"> <strong><em>“Sesungguhnya , yang paling  baik dan apa yang kamu makan adalah yang  berasal dari kerjamu dan sesungguhnya anak-anakmu adalah dari usahamu.”</em> (Al-Hadits)</strong></div>

    <div class="style2"> <strong>b. UNSUR KETERAMPILAN</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Mengandalkan berpikir saja  belumlah cukup untuk dapat mewujudkan suatu karya nyata. <strong>Karya </strong>hanya <strong>terwujud</strong> bila  ada <strong>tindakan</strong>. <strong>Keterampilan merupakan tindakan raga</strong> untuk <strong>melakukan suatu kerja</strong>.  Dan hasil kerja itu baru dapat  diwujudkan suatu karya, baik berupa produk maupun jasa.<br />
      <br />
      <div style="text-align:center"> <a href="http://2.bp.blogspot.com/-yBAXV2rG64M/UBOENlRQq7I/AAAAAAAAA00/UuSYbYQ-6uQ/s1600/creative.jpg" imageanchor="1"><img src="http://2.bp.blogspot.com/-yBAXV2rG64M/UBOENlRQq7I/AAAAAAAAA00/UuSYbYQ-6uQ/s320/creative.jpg" alt="" width="320" height="240" border="0" /></a></div>
    </div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Islam memberikan perhatian besar  pada penguasaan keterampilan. <strong>Penguasaan  keterampilan</strong> yang serba material ini juga merupakan <strong>tuntutan yang harus dilakukan setiap muslim</strong> dalam rangka pelaksanaan tugasnya. </div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Secara normative, terdapat banyak <strong>nash dalam Al-Qur’an</strong> dan <strong>hadist</strong> yang menganjurkan untuk <strong>mempelajari ilmu-ilmu pengetahuan umum atau  keterampilan:</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“Dan  carilah pada apa yang telah dianugerahkan Allah kepadamu (kebahagiaan) negeri  akhirat, dan jangan kamu melupakan bagianmu dari (kenikmatan) duniawi…” (Al-Qashash: 77)</em></strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“Dan,  siapkanlah untuk menghadapi mereka kekuatan apa saja yang kamu sanggupi dan  kuda-kuda yang ditambat untuk berperang…(Al-Anfaal:  60)</em></strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“Hiasilah  wanita-wanita kalian dengan ilmu tenun,” (HR.  Al-Khatib dari Ibnu Abbas r.a)</em></strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“ Dan,  buatlah bahtera itu dengan pengawasan dan petunjuk wahyu Kami, dan janganlah  kamu bicarakan dengan Aku tentang orang-orang yang zalim itu. Sesungguhnya,  mereka itu akan ditenggelamkan.” (Huud:37)</em></strong></div>
    <div class="style2"> <br />
    </div>

    <div class="style2"> <strong>c. UNSUR SIKAP MENTAL MAJU</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Daya pikir dan keterampilan belumlah dapat menjamin kesuksesan, <strong>sukses hanya  dapat diraih jika terjadi sinergi antara pemikiran, keterampilan, dan sikap  mental maju.</strong> Sikap mental maju inilah yang dalam banyak hal justru  menjadi penentu keberhasilan seseorang</div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> Bagi seorang  muslim,  sikap mental maju pada hakikatnya merupakan <strong>konsekuensi dari tauhid dan buah  kemuslimannya dalam seluruh aktivitas kesehariannya</strong>. Identitas itu  tampak pada kepribadian muslim, yakni pada pola pikir (aqliyah) dan pola  bersikapnya (nafsiyyah) yang dilandaskan pada aqidah Islam. Disini tampak jelas  bahwa sikap mental maju sesungguhnya adalah sebuah dari <strong>pola sikap yang didorong secara produktif  oleh pola pikir Islami.</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“ Tidak beriman salah seorang diantara  kalian hingga aku menjadi akalnya yang ia berpikir dengannya.”</em></strong> <strong>(Hadist Qudsi)</strong></div>
    <div class="style2"> <br />
    </div>
    <div class="style2"> <strong><em>“Tidaklah beriman salah seorang di antara  kalian, sehingga dia menjadikan hawa nafsunya mengikuti apa-apa (dinul Islam)  yang kubawa.”</em></strong> <strong>(Hadits Arba’in an’Nawawiyyah)</strong></div>

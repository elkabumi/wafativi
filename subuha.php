        <p align="justify"><br />
          <br />
          <span class="style2">Allah SWT bersumpah dalam Al Fajr :“Demi fajar (waktu Subuh)”. Kemudian dalam Al Falaq Allah mengingatkan:“Katakanlah! aku berlindung kepada Tuhan yang menguasai waktu subuh”.<br />
          <br />
          Ada apa sebenarnya di balik waktu Subuh? Mengapa Allah sampai bersumpah demi waktu Subuh? Dan mengapa pula kita harus berlindung kepada yang menguasai waktu Subuh? Apakah waktu Subuh sangat berbahaya?<br />
          <br />
          Ya, ternyata waktu Subuh memang benar-benar sangat berbahaya! Waktu Subuh itu lebih kejam dari sekawanan perampok bersenjata api. Waktu Subuh bisa lebih menyengsarakan dari derita kemiskinan. Waktu Subuh bahkan lebih berbahaya dari kobaran api yang disiram bensin!<br />
          <br />
          Saudaraku,<br />
          <br />
          Jika ada sekawanan perampok menyatroni rumah kita, dan mengambil paksa semua barang yang kita miliki. Emas dan semua perhiasan dirampas. Uang cash puluhan juta dirampas. Laptop yang berisi data-data penting dirampas. Mobil yang belum lunas cicilan-nya juga dirampas.Nah,bisa dibayangkan bagaimana pedihnya hati kita menerima kenyataan itu?<br />
          <br />
          Tapi ketahuilah, sebenarnya waktu Subuh lebih kejam dari perampok itu. Sebab jika kita ‘tergilas waktu Subuh’, sehingga melalaikan shalat fajar, maka kita akan menderita kerugian yang jauh lebih besar dari sekadar kehilangan laptop dan mobil. Kita bahkan akan kehilangan dunia dan segala isinya. Ingat,“Dua rakaat fajar lebih baik dari dunia dan segala isinya,” (HR Muslim).<br />
          <br />
          Waktu Subuh juga bisa lebih menyeng-sarakandari sekadar kemiskinan di dunia. Sebab bagi orang-orang yang ‘tergilas waktu Subuh’, sehingga mengabaikan shalat Subuh berjamaah di masjid, maka pada hakikatnya, merekalah orang-orang miskin sejati yang hanya mendapatkan upah 1/150 (0,7%) saja dari pahala shalatnya.<br />
          “… dan barangsiapa yang shalat Subuh berjamaah, maka ia bagaikan sholat semalam suntuk” (HR Muslim).<br />
          <br />
          Saudaraku,<br />
          <br />
          Shalat semalam suntuk adalah shalat yang dikerjakan mulai dari tenggelamnya matahari sampai terbit fajar. Fantastis! Shalat selama sepuluh jam, atau kurang lebih, sama dengan 150 kali shalat! Jadi, betapa agungnya fadilah shalat Subuh berjamaah ini, khususnya bagi Kaum Adam. Dan betapa malangnya orang yang ‘tergilas waktu Subuh’, orang-orang yang mengabaikan shalat subuh berjamaah di masjid.<br />
          <br />
          Waktu Subuh juga lebih berbahaya dari kobaran api yang disiram bensin. Mengapa demikian? Karena Rasulullah telah bersabda, bahwa orang yang tidak mampu melaksa-nakan shalat Subuh berjamaah, kedudukan-nya setara dengan orang munafik.<br />
          <br />
          “Sesungguhnya tiada yang dirasa berat oleh seorang munafik, kecuali melaksanakan shalat Isya dan shalat Subuh. Sekiranya mereka tahu akan keagungan pahalanya, niscaya mereka bakal mendatanginya (ke masjid, shalat berjamaah) sekalipun harus berjalan merangkak-rangkak” (HR Bukhari Muslim).<br />
          <br />
          Saudaraku,<br />
          <br />
          Orang yang ‘tergilas waktu Subuh’ sehingga tak mampu mendatangi masjid untuk shalat berjamaah, sesungguhnya adalah orang yang dalam keadaan terancam bahaya, karena dirinya disetarakan dengan orang munafik. Sebab, ancaman bagi orang munafik adalah Neraka Jahanam.“Sesungguhnya Allah akan mengumpulkan orang-orang munafik dan orang-orang kafir di dalam Jahanam” (An Nisa:140).<br />
          <br />
          Bukankah Jahanam lebih berbahaya dari sekadar kobaran api yang disiram bensin?<br />
          <br />
          Nah, agar kita tidak merasakan ‘gilasan waktu Subuh’ yang lebih kejam dari perampokan, agar kita tidak terkena ‘gilasan waktu Subuh’ yang lebih menyengsarakan dari derita kemiskinan, dan agar kita tidak terkapar ‘gilasan waktu Subuh’ yang lebih berbahaya dari kobaranapi, maka:“Katakanlah! Aku berlindung kepada Tuhan yang menguasai waktu Subuh” (Al Falaq:1).Yaitu dengan memanfaatkan waktu Subuh sebaik-baiknya.Lakukan shalat sunnah (shalat fajar) dan shalat berjamaah di masjid terutama bagi laki-laki.<br />
          <br />
          Saudaraku,<br />
          <br />
          Tapi ada satu pertanyaan yang tersisa, yaitu mengapa waktu Subuh bisa menggilas kita? Sebab, kalau kita mau memperhatikan, sesungguhnya waktu Subuh adalah waktu yang paling berat buat kita yang ingin beribadah. Mengapa? Karena waktu Subuh adalah saat paling tenang, sehingga sangat pas buat menenggelamkan diri dalam tidur nyenyak dan bermimpi indah. Ditambah lagi, umumnya, suhu udara waktu Subuh lebih dingin dibanding waktu lainnya – sehingga membuat yang sedang tidur jadi makin merapatkan selimutnya.<br />
          <br />
          Tapi yang paling berbahaya ialah, kalau kita sampai kena kepung pasukan iblis – yang akan mengencingi telinga kita, agar tak bisa mendengar panggilan “Shalat itu lebih baik daripada tidur!” dari speaker masjid, dan meniup-niup mata kita – sehingga mata kita serasa dilem atau kelopaknya bagaikan dibanduli dengan beban jutaan ton, sehingga “susaaaahhhh betul buat dibuka”.<br />
          <br />
          Nah, suasana waktu Subuh dan hasil daya upaya pasukan iblis itulah yang akan “menggilas” niat kita, tekad kita, buat bangun dan mendirikan sholat. Sehingga kita terus terkapar tanpa daya di atas buaian tidur lelap dan mimpi indah, dan melalaikan shalat fajar serta shalat Subuh berjamaah di masjid. Jadi, jangan anggap remeh kekuatan waktu Subuh dalam mematahkan atau menggilas iman kita, sehingga iman kita tidak bisa bertumbuh dengan subur oleh siraman fadilah shalat fajar dan shalat berjamaah.<br />
          <br />
          Wallahu A’lam. [Sumber: Kajian Islam]</span></p>
